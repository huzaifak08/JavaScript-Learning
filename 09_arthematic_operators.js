//Arthematic Operators:

let a = 45;
let b = 4;

// Basic One's:
console.log("Addition of a and b is: ", a + b)
console.log("Subtraction of a and b is: ", a - b)
console.log("Multiplication of a and b is: ", a * b)
console.log("Division of a and b is: ", a / b)

// Important One's:
console.log("Exponential of a and b is: ", a ** b) // a ki power b || 45 power 4
console.log("Modulus of a and b is: ", a % b)

// Increment/Decrement: a++ will first take the value of a and then increment.
console.log("Increment of a is: ", a++);
console.log('Value of a: ', a)

console.log("Decrement of b is: ", b--)
console.log('Value of b: ', b)

// ++a will first increment and then take the value of a.
console.log("Value of ++a is: ", ++a);
console.log("Value of --b is: ", --b);