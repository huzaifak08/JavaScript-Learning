// Strings:

let fname = 'Huzaifa';
let lname = "Khan";

// String Interpolation:
let result = `First Name is ${fname} and Last Name is ${lname}`;  // backticks used here instead of quotes.
console.log(result);

// Escape Sequence Characters:
let fruit = 'Banana\'s';    // Banana's
console.log(fruit);

/* 
    \' --> '
    \" --> "
    \\ --> \
    \n --> New Line
    \b --> Backspace
    \f --> Form Feed
    \r --> Carriage Return
    \t --> Horizontal Tab
    \v --> Vertical Tab
*/